//Bucle while
//Ejemplo: tablas de multiplicar del 2

var condicion = 2;
var total = condicion;

while (condicion < 11) {

    console.log("tabla: " + "2 x " + condicion + " = " + (condicion * total)); 
    condicion = condicion + 1;
}

