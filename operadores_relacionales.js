//Operadores relacionales sirven para validar informacion

// == comparacion
// > Mayor que
// < Menor que
// >= Mayor o igual que
// <= Menor o igual que

//ejemplo: como saber si una persona es mayor de edad

var edad = 18;

if (edad >= 18) {
    console.log("Es mayor de edad");
}

//ejemplo de comparacion

var precio = 20;

if (precio == 20) {
    console.log("Se realiza la compra");
}

//ejemplo con operador booleano

var condicion = true;

if (condicion) {
    console.log("Sentencia verdadera");
}

























